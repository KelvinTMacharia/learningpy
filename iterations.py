# iteration - repetition ; lists, tuple, dictionary, string
# indentation Are spaces used to define our block of code( 1 tab or press space 4 times)
"""
Types of iteration
1. for loop
syntax
        for <variableName> in sequence:
            block of code to be executed.

Example
List = [1,2,3,4,5,6]
for each in list:
print(each)
# 1st iteration

2. while loop
"""
for k in range(1,101):
    print(k)

for N in "Kelvin":
    print(N)

for t in range(0,101,2):
    print(t)

"""
While Loop only executes when the condition is true

Syntax
while <condition>:
    block of code to be executed.
    
    Always increment a while loop to avoid trouble

"""
i = 1
while i <=10:
    print(i)
    i=i+1

